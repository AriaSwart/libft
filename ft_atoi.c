int		ft_atoi(char *nbr)
{
	int i;
	int sign;
	int n;

	sign = 1;
	i = 0;
	n = 0;
	if (!nbr)
		return (0);
	while ((nbr[i] != '\0') && !('0' <= nbr[i] && nbr[i] <= '9'))
	{
		if ('0' <= nbr[i + 1] && nbr[i + 1] <= '9')
			sign = -1;
		i++;
	}
	while ((nbr[i] != '\0') && '0' <= nbr[i] && nbr[i] <= '9')
	{
		n = (n * 10) + (nbr[i] - 48);
		i++;
	}
	return (n * sign);
}
